import csv
import re

CSV_PATH = "DTA-TABLA POR PROVINCIA-CANTÓN-DISTRITO 2022.csv"
OUTPUT_PATH = "db_seed_regions.sql"

cantons = {}   # canton_code -> canton_name (ordered by first appearance)
canton_order = []  # ordered list of canton_codes
districts = []  # list of (district_name, canton_code)

with open(CSV_PATH, encoding="utf-8-sig") as f:
    reader = csv.reader(f)
    for row in reader:
        if len(row) < 7:
            continue
        canton_code = row[3].strip()
        canton_name = row[4].strip()
        district_name = row[6].strip()

        # skip header rows
        if not canton_code.isdigit():
            continue

        if canton_code not in cantons:
            cantons[canton_code] = canton_name
            canton_order.append(canton_code)

        districts.append((district_name, canton_code))

# map canton_code -> sequential id (1-based, insertion order)
canton_id = {code: i + 1 for i, code in enumerate(canton_order)}

# find Montes de Oca position for constraint validation
mdo_id = None
for code, name in cantons.items():
    if "montes de oca" in name.lower():
        mdo_id = canton_id[code]
        break

lines = []
lines.append("-- Auto-generated by generate_catalog_seed.py from INEC 2022 data")
lines.append(f"-- Montes de Oca = canton id {mdo_id} (constraint addresses.residence_canton_consistency must match)")
lines.append("")

# cantons insert
lines.append("INSERT INTO public.cantons (name) VALUES")
for i, code in enumerate(canton_order):
    comma = "," if i < len(canton_order) - 1 else ""
    name = cantons[code].replace("'", "''")
    lines.append(f"  ('{name}'){comma}")
lines.append("ON CONFLICT (name) DO NOTHING;")
lines.append("")

# districts insert
lines.append("INSERT INTO public.districts (name, fk_canton) VALUES")
for i, (dname, ccode) in enumerate(districts):
    comma = "," if i < len(districts) - 1 else ""
    safe = dname.replace("'", "''")
    fk = canton_id[ccode]
    lines.append(f"  ('{safe}', {fk}){comma}")
lines.append("ON CONFLICT (name, fk_canton) DO NOTHING;")
lines.append("")

with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
    f.write("\n".join(lines))

print(f"Generated {OUTPUT_PATH}")
print(f"  Cantons : {len(canton_order)}")
print(f"  Districts: {len(districts)}")
print(f"  Montes de Oca id: {mdo_id}")
